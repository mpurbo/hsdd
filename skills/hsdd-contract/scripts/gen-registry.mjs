#!/usr/bin/env node
// gen-registry.mjs
// HSDD registry generator. Projects YAML frontmatter from contract and ADR
// markdown files into deterministic INDEX.md tables. Zero dependencies.
//
// Usage:
//   node hsdd/scripts/gen-registry.mjs    # scan ./hsdd/contract and ./hsdd/adr under cwd
//   node gen-registry.mjs --root <dir>    # scan <dir>/contract and <dir>/adr
//
// Output is deterministic (no timestamps), so re-running produces no spurious
// diffs. Wire it into a pre-commit or CI hook after editing contracts.

import { readdirSync, readFileSync, writeFileSync, existsSync } from "node:fs";
import { join } from "node:path";

function parseArgs(argv) {
  const args = { root: join(process.cwd(), "hsdd") };
  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === "--root") args.root = argv[++i];
  }
  return args;
}

const dequote = (s) => s.replace(/^["']|["']$/g, "");
const stripComment = (s) => s.replace(/\s+#.*$/, "").trim();

// Minimal frontmatter parser: scalars, inline [a, b] lists, and block "- item"
// lists. Strips surrounding quotes and trailing " # comments".
function parseFrontmatter(text) {
  if (!text.startsWith("---")) return null;
  const end = text.indexOf("\n---", 3);
  if (end === -1) return null;
  const data = {};
  let key = null;
  for (const raw of text.slice(3, end).split("\n")) {
    if (!raw.trim() || raw.trim().startsWith("#")) continue;
    const item = raw.match(/^\s*-\s+(.*)$/);
    if (item && Array.isArray(data[key])) {
      data[key].push(dequote(stripComment(item[1])));
      continue;
    }
    const kv = raw.match(/^([A-Za-z0-9_]+):\s*(.*)$/);
    if (!kv) continue;
    key = kv[1];
    const val = stripComment(kv[2]);
    if (val === "") { data[key] = []; }                     // block list follows
    else if (val.startsWith("[")) {                         // inline list
      data[key] = val.replace(/^\[|\]$/g, "").split(",").map((s) => dequote(s.trim())).filter(Boolean);
    } else { data[key] = dequote(val); }
  }
  return data;
}

function loadDir(dir) {
  if (!existsSync(dir)) return [];
  return readdirSync(dir)
    .filter((f) => f.endsWith(".md") && f.toUpperCase() !== "INDEX.MD")
    .map((f) => ({ file: f, data: parseFrontmatter(readFileSync(join(dir, f), "utf8")) }))
    .filter((e) => e.data)
    .sort((a, b) => (a.data.id || a.file).localeCompare(b.data.id || b.file));
}

const count = (v) => (Array.isArray(v) ? v.length : v ? 1 : 0);
const list = (v) => (Array.isArray(v) ? v.join(", ") : v || "");

function writeTable(path, title, header, sep, rows) {
  const out = [
    "<!-- generated by gen-registry.mjs - do not edit by hand -->",
    "",
    `# ${title}`,
    "",
    header,
    sep,
    ...rows,
    "",
  ].join("\n");
  writeFileSync(path, out);
}

function genContracts(root) {
  const dir = join(root, "contract");
  const entries = loadDir(dir);
  if (entries.length === 0) return null;
  const rows = entries.map(({ data: d }) =>
    `| \`${d.id}\` | ${d.version || ""} | ${d.kind || ""} | ${d.status || ""} | \`${d.owner || ""}\` | ${count(d.consumers)} |`);
  writeTable(join(dir, "INDEX.md"), "Contract Registry",
    "| id | version | kind | status | owner | consumers |",
    "|----|---------|------|--------|-------|-----------|", rows);
  return entries.length;
}

function genAdrs(root) {
  const dir = join(root, "adr");
  const entries = loadDir(dir);
  if (entries.length === 0) return null;   // no frontmatter ADRs: skip silently
  const rows = entries.map(({ data: d }) =>
    `| \`${d.id}\` | ${d.status || ""} | ${list(d.affects)} |`);
  writeTable(join(dir, "INDEX.md"), "ADR Registry",
    "| id | status | affects |", "|----|--------|---------|", rows);
  return entries.length;
}

const { root } = parseArgs(process.argv.slice(2));
const c = genContracts(root);
const a = genAdrs(root);
console.log(`contract: ${c === null ? "none" : c + " -> contract/INDEX.md"}`);
console.log(`adr:      ${a === null ? "none" : a + " -> adr/INDEX.md"}`);
